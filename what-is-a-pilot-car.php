				<h1>What is a Pilot Car?</h1>
				<i>July 10, 2009</i>
				<p>When shipping an oversize load, it is not only important to make it on time, but to make it there safely. With respect to pilot car safety, pilot car companies need to have the right equipment, up-to-date information on weather &amp; transportation, and a good driving team to prevent any accidents or delays from occurring.</p>
				<p>In some areas the pilot car must be in front of the load, and in others they need to be behind the load. Generally this depends on what state you are in and what route you are traveling. Some loads that are wider than 14 ft will need 2 pilot cars, one in front of the load and one in the rear.</p>
				<p>When the load is over height, some states require a special pilot car that is called a "Height Pole Car". This type of car has a special telescopic pole attached to it, that is adjustable to whatever height is needed for that particular load. Generally these cars are required if the loaded height is more than 17 ft. However, there are some states that require this type of car at lower measurements, such as New York requires them if the loaded height is more than 14 ft 6 in.</p>
				<p>In most states the pilot car does not need to be certified, but they do need to have specific equipment. Such as flags, flashing lights, cones, orange vests, and other safety equipment. There are states that the pilot car must be certified for. Utah, Washington, New York &amp; Florida the pilot car must be certified by the state. In Pennsylvania the pilot car must meet specific insurance requirements.</p>
